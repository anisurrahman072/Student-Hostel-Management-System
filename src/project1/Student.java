    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Hostel_Id=jTextField1.getText();
        String Password=jPasswordField1.getText();
        
        PreparedStatement pstmt=null;
        ResultSet rs=null;
        Connection con=null;
        int count=0;
        
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            String path="jdbc:mysql://localhost/DBProject1";
            String user="root";
            String pass="123456";
            con=DriverManager.getConnection(path,user,pass);
            
            pstmt=con.prepareStatement("select * from InsertNewStudent where Hostel_Id=? and Password=?");
            pstmt.setString(1,Hostel_Id);
            pstmt.setString(2,Password);
            
            rs=pstmt.executeQuery();
            while(rs.next())
            {
                pstmt=con.prepareStatement("select * from insertstudentmealinfo where Hostel_Id=?");
                pstmt.setString(1,Hostel_Id);
                rs=pstmt.executeQuery();
                while(rs.next())
                {
                    jTextField3.setText(rs.getString("Bill"));
                    jTextField4.setText(rs.getString("Breakfast"));
                    jTextField5.setText(rs.getString("Lunch"));
                    jTextField6.setText(rs.getString("Dinner"));
                    
                }
                count++;
            }
            if(count==0)
            {
                jTextField1.setText("");
                jPasswordField1.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                JOptionPane.showMessageDialog(null," Incorrect User!!! ");
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null," Error Found "+ex);
        }
        finally
        {
            try
            {
                con.close();
                pstmt.close();
                rs.close();
            }
            catch(Exception e)
            {
                
            }
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

